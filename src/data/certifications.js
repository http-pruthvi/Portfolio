// Helper functions for certifications data
export const getCertificationsByCategory = (category) => {
    return certificationsData.filter(cert => cert.category === category);
};

export const getActiveCertifications = () => {
    const now = new Date();
    return certificationsData.filter(cert => {
        if (!cert.expiryDate) return true; // No expiry date means it's always active
        return new Date(cert.expiryDate) > now;
    });
};

export const getExpiredCertifications = () => {
    const now = new Date();
    return certificationsData.filter(cert => {
        if (!cert.expiryDate) return false; // No expiry date means it never expires
        return new Date(cert.expiryDate) <= now;
    });
};

export const getCertificationCategories = () => {
    return [...new Set(certificationsData.map(cert => cert.category))];
};

export const getSortedCertifications = () => {
    return certificationsData.sort((a, b) => {
        const dateA = new Date(a.issueDate);
        const dateB = new Date(b.issueDate);
        return dateB - dateA; // Most recent first
    });
};